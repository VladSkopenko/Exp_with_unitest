def add(a, b):
    """
    The add function adds two numbers together.

    :param a: Store the first number that is passed into the function
    :param b: Add the value of b to a
    :return: The sum of a and b
    :doc-author: Trelent
    """
    return a + b


def sub(a, b):
    """
    The sub function subtracts two numbers.

    :param a: Store the first number entered by the user
    :param b: Subtract the value of a from b
    :return: The difference between a and b
    :doc-author: Trelent
    """
    return a - b


def mul(a, b):
    """
    The mul function multiplies two numbers together.

    :param a: Multiply the b parameter
    :param b: Multiply the a parameter by b
    :return: The product of a and b
    :doc-author: Trelent
    """
    return a * b


def div(a, b):
    """
    The div function divides two numbers.

    :param a: Divide the b parameter
    :param b: Divide the a parameter by
    :return: The quotient of a and b
    :doc-author: Trelent
    """
    return a / b
